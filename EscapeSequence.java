public class EscapeSequence {
    public static void main(String args[]){

        System.out.println("I am escape sequence \' double slash");
        System.out.println("I am escape sequence \\ double slash");
        System.out.println("I am escape sequence \" double slash");
        System.out.println("I am escape sequence \t double slash");
        System.out.println("I am escape sequence \r double slash");
        System.out.println("I am escape sequence \f double slash");
        System.out.println("I am escape sequence \b double slash");

    }
    
}
