public class Resulting_Datatype {
    public static void main(String args[]){

        /* byte +short = int
           short +int =int
           long + float =float
           int + float =float
           char + int =int
           char + short =int
           long + double=double
           float + double =double
         */
        int a = 23 + 187;
        System.out.println(a);

        int b = 186 + 808;
        System.out.println(b);

        float c = 134759534L+ 7.8f;
        System.out.println(c);

        float d = 4.5f + 4;
        System.out.println(d);

        int f = 'A' + 32;
        System.out.println(f);

        int g = 'c' + 12;
        System.out.println(g);

        double h = 1234556L + 23.8889d;
        System.out.println(h);

        double i = 12.96 + 24.5679;
        System.out.println(i);

    }

}
